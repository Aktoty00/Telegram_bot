text_bf2 = """One-cup pancakes with blueberries\n
Ingredients:
1 large free-range egg
1 cup of self-raising flour
1 cup of milk
200 g blueberries
olive oil
4 tablespoons natural yoghurt\n
Method:
1. Crack the egg into a large mixing bowl, add the flour, milk and a tiny pinch of sea salt.\n
2. Whisk everything together until you have a lovely smooth batter, then fold through the blueberries.\n
3. Heat ½ a tablespoon of olive oil in a large non-stick frying pan over a medium heat, carefully tilting the pan to spread the oil out evenly.\n
4. Add a few ladles of batter to the pan, leaving space between each one so they have room to spread out slightly – each ladleful will make one pancake, 
and you’ll need to work in batches.\n
5. Cook for 1 to 2 minutes, or until little bubbles appear on the surface and the bases are golden, then carefully flip them over.\n
6. When the pancakes are golden on both sides, remove to a plate.\n
7. Repeat with the remaining batter, adding a little more oil to the pan between batches, if needed.\n
8. Serve the pancakes while they’re still hot, with a dollop of yoghurt and some extra fresh berries, if you like."""
